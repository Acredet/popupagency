        { position: { lat: -0.48585, lng: 117.1466 } },
        { position: { lat: -6.9127778, lng: 107.6205556 } }
      ]
    }
  },
  watch: {
    'filters.price': {
      deep: true,
      handler (val) {
        console.log(val)
        if (!val.min && !val.max) {
          this.filters.price.text = 'Price'
        } else if (val.min && !val.max) {
          this.filters.price.text = `Min $${val.min}`
        } else {
          this.filters.price.text = `Max $${val.max}`
        }
      }
    },
    'filters.property.choose': {
      deep: true,
      handler (val) {
        console.log(val)
        if (val.length === 0) {
          this.filters.property.text = 'Property type'
        } else if (val.length === 8) {
          this.filters.property.text = 'Any'
        } else {
          this.filters.property.text = val.join()
        }
      }
    }
  },
  async mounted () {
    await this.$axios.$get('/places')
      .then((res) => {
        this.cards = res.data.map((x) => {
          return {
            images: x.cover,
            place: x.stad,
            money: `fr ${x.prispermanad} kr / månad`,
            text: x.beskreving
          }
        })
      })
      .catch(err => console.log(err))
  },
  methods: {
    toggleAll (index) {
      this.filters.plats.tabs[index].selected =
        this.filters.plats.tabs[index].selected.length !== this.filters.plats.tabs[index].options.length
          ? this.filters.plats.tabs[index].options.slice()
          : []
    },
    placeChoose (index) {
      console.log(this.filters.plats.tabs[index].selected.length)
      this.$nextTick(() => {
        if (this.filters.plats.tabs[index].selected.length === 0) {
          this.filters.plats.tabs[index].indeterminate = false
          this.filters.plats.tabs[index].allSelected = false
        } else if (
          this.filters.plats.tabs[index].selected.length === this.filters.plats.tabs[index].options.length
        ) {
          this.filters.plats.tabs[index].indeterminate = false
          this.filters.plats.tabs[index].allSelected = true
        } else {
          this.filters.plats.tabs[index].indeterminate = true
          this.filters.plats.tabs[index].allSelected = false
        }
      })
    },
    addProperty (button) {
      if (this.filters.property.choose[button.text]) {
        this.filters.property.choose[button.text] = false
      } else {
        this.filters.property.choose[button.text] = true
      }
    }
  }
}
