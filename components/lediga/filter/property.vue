<template>
  <b-button-group v-if="icons.length > 0" vertical class="d-flex flex-wrap align-items-center justify-content-between flex-row w-100">
    <b-button
      v-for="(icon) in icons"
      :key="icon.text"
      :pressed.sync="icon.state"
      variant="outline-primary"
      squared
      :style="{ width: sideBar ? '100%' : '45%' }"
      class="text-left m-1"
      @click="clicked(icon)"
    >
