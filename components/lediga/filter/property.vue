      {{ icon.text }}
    </b-button>
  </b-button-group>
</template>

<script>
export default {
  props: {
    sideBar: {
      type: Boolean,
      default: () => false
    },
    icons: {
      type: Array,
      default: () => []
    }
  },
  methods: {
    clicked (icon) {
      this.$emit('iconChoosed', icon)
    }
  }
}
